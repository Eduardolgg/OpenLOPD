package com.openlopd.common.localizacion.entities;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;

/**
 * Entidad encargada del almacenamiento de las localidades españolas
 *
 * @author Eduardo L. García Glez.
 * @version 0.0.0 09 de marzo de 2011
 */
@Entity
@Table(name = "aux_localidades")
@NamedQueries({
    @NamedQuery(name = "Localidad.findByProvincia", query = "SELECT l FROM Localidad l WHERE l.provincia.id = :codProvincia"),
    @NamedQuery(name = "Localidad.findByText", query =
    "SELECT DISTINCT(l.localidad) FROM Localidad l WHERE UPPER(l.localidad) like :term ")
})
public class Localidad implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    @ManyToOne
    @JoinColumn(name = "cod_provincia", nullable = false)
    private Provincia provincia;
    @Column(nullable = false, length = 100)
    private String localidad;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getLocalidad() {
        return localidad;
    }

    public void setLocalidad(String localidad) {
        this.localidad = localidad;
    }

    public Provincia getProvincia() {
        return provincia;
    }

    public void setProvincia(Provincia provincia) {
        this.provincia = provincia;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Localidad)) {
            return false;
        }
        Localidad other = (Localidad) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.openlopd.common.localizacion.entities.Localidad[id=" + id + "]";
    }
}
